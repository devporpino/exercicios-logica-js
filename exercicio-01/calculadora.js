const x = parseFloat(prompt("Informe um número: "))
const y = parseFloat(prompt("Informe outro número: "))

const soma = x + y
const subtracao = x - y
const multiplicacao = x * y
const divisao = x / y

alert(
    "Resultado: \n" + 
    "\nSoma: " + soma + 
    "\nSubtração: " + subtracao + 
    "\nmultiplicação: " + multiplicacao + 
    "\nDivisão: " + divisao
)
